import Foundation
import UIKit

extension UIColor {
    
    class func navigationBackground() -> UIColor {
        return UIColor(red: 22/255, green: 30/255, blue: 61/255, alpha: 1.0)
    }
    
    class func textColor() -> UIColor {
        return UIColor(red: 251/255, green: 247/255, blue: 254/255, alpha: 1.0)
    }
}
